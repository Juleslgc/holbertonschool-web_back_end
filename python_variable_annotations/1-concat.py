"""
It contains the function:
concat()
"""


def concat(str1: str, str2: str) -> str:
    """
    A function that concatenante
    two strings

    Args:
        str1 (str): First string
        str2 (str): Second string

    Return:
        The new string of str1 and str2
    """
    return str1 + str2
